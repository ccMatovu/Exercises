public class GettingIndividualDigits {
    public static void main(String[] args) {
        int count =10;
            for(int j=1;j<10;j++){
                for(int z=0; z<10;z++){
                    System.out.println(count+", "+j+"+"+z+" = "+(j+z));
                    count++;
                }
            }

    }
}
